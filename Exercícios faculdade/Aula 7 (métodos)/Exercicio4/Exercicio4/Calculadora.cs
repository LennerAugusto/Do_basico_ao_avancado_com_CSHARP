using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Exercicio4
{
    internal class Calculadora
    {
        public int a;
        public int b;  

        public int soma()
        {
            return a + b;
        }

        public int divide()
        {
            return a / b;
        }

        public int multiplica()
        {
            return a * b;
        }

        public int subtrai()
        {
            return a - b;
        }
    }
}
